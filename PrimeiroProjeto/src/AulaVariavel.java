

public class AulaVariavel {
    public static void main(String[] args) {
        // Variaveis Grupo Inteiro
        byte numeroPequeno = 125;
        short numeroShort = 32767;
        int numeroMaior = 2147483647;
        long numeroEnorme = 922337203685477000l;

        // Variaveis Grupo Flutuante
        float peso = 159.3f;
        double pi = 3.14128;

        // Variaveis do tipo caracter
        char letra = 'T';

        // Variaveis Booleano
        boolean estouComFome = true;

        // Variaveis do tipo String, ela eh do tipo class
        String nome = "Thiago";


        
    }
}   